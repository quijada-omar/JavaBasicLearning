package day2;

public class DataTypesDemo2 {
	public static void main(String[] args) {
		
		int a = 100, b = 200;
		System.out.println("The value of a is: " + a);
		System.out.println("The value of b is: " + b);
		
		long x = 1234567889;
		System.out.println(x);
		
		byte by = -100;
		System.out.println(by);
		
		short sh = 32766;
		System.out.println(sh);
		
		// Decimal numbers - float double
		
		float item_price = 15.50f;
		System.out.println(item_price);
		
		double d = 1234.1234567;
		System.out.println(d);
		
		double du = 125; // Valid
		System.out.println(du);
		
//		int it = 10.50; //Not valid
//		System.out.println(it);
		
		char grade = 'A';
		System.out.println("The Grade of student is" + grade);
		
		boolean bo = true;
		System.out.println(bo);
		
		String person_name = "John Doe";
		System.out.println("The name of the person is " + person_name);
		
		String str = "A"; //Valid
		System.out.println(str);
		
//		char ch = 'ABC'; // Invalid
		
		}
}
